package com.webapplication.JSONBeans;

public class KeyAndValueBean {

	
	private Long key;
	private String value;
	
	public KeyAndValueBean() {
		
	}

	public KeyAndValueBean(Long key, String value) {
		
		this.key = key;
		this.value = value;
	}

	public Long getKey() {
		return key;
	}

	public void setKey(Long key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	
	
	
	
}
